		<P>
	З метою залучення додаткових коштів на зміцнення матеріально-технічного та соціального розвитку Центральної районної поліклініки Деснянського району м. Києва, відповідно до <a target="_blank" href="http://zakon1.rada.gov.ua/laws/anot/531/97-%D0%B2%D1%80">Закону України від 16.09.1997 року №531 97 - ВР</a>  "Про благодійництво та благодійні організації" та згідно <a  target="_blank"  href="http://zakon2.rada.gov.ua/laws/show/1222-2000-%D0%BF">Постанови Кабінету Міністрів України №1222</a> від 04.08.2000 року "Про затвердження порядку отримання благодійних (добровільних) внесків і пожертв від юридичних та фізичних осіб бюджетними установами і закладами освіти, охорони здоров'я, культури, науки, спорту та фізичного виховання  для потреб їх фінансування" визначено благодійників, 
		які можуть надавати благодійні внески через реєстраційний рахунок №35426004001190 в ГУДКСУ м. Києва, МФО 820019, код ЄДРПОУ 26188308:
<UL>
<LI>1.Фізичні особи:
 <OL>1.1. які не є громадянами України;</OL>
<OL>1.2. громадяни України, які не прописані (не зареєстровані) у м. Києві;</OL>
<OL>1.3. громадяни України, які прописані у м. Києві на території, не прикріпленій до району обслуговування ЦРП Деснянського району м. Києва;</OL>
<OL>1.4. інші.</OL></LI>

<LI>2. Благодійні організації зареєстровані у встановленому законодавством порядку.</LI>
<LI>3. Підприємства (організації).</LI></UL>

  <B>Благодійництво та благодійна діяльність здійснюється у таких основних напрямках:</B>
<UL><LI>Сприяння практичному здійсненню розвитку матеріально-технічної бази та соціального розвитку Центральної районної поліклініки Деснянського району м. Києва;</LI>
<LI>Поліпшення медикаментозного забезпечення при наданні медичної допомоги незахищеним верствам населення, інвалідам, та іншим особам;</LI>
<LI>Ремонт та підтримка медичного обладнання у належному стані;</LI>
<LI>Інші невідкладні заходи, які необхідні для належного надання медичної допомоги.</LI></UL></P><br>
                                 

