        <tr><td><img class="image_left" align="left" src="images/surgery1.jpg" width="200" height="138" border="0" alt=""> 
<P>Великий досвід роботи, висока кваліфікація хірургів та новітні методики лікування відкривають нові можливості для амбулаторної хірургії, дозволяючи їй вирішувати все більш широкий спектр питань, пов'язаних зі здоров'ям людини. 
І головне - все це робиться в амбулаторних умовах, швидко і безболісно.</P>	
<P>Протягом останніх років кваліфіковану і спеціалізовану хірургічну медичну допомогу хворим надає спеціально створений у поліклініці підрозділ. 
Особливістю такої форми надання допомоги є короткий період очікування операції, психологічний та фізичний комфорт (відсутність негативного впливу лікарняної обстановки), 
кращий медико-гігієнічний догляд за післяопераційною раною, збереження звичайного ритму життя, зручність для пацієнта, можливість постійної допомоги рідних, 
отриманя кращих показників якості життя прооперованих хворих. Використання запропонованих хірургічних методів втручання, економічний ефект, 
свідчать про <b>безперечні переваги хірургічного лікування хворих в умовах стаціонару одного дня</b>. 
</P>
<P>В даний час більшість прооперованих хворих короткочасно перебувають в палаті (кілька годин), а потім відправляються додому, де і проходить їх остаточне одужання.
</P>	
<P>Популярність амбулаторної хірургії пов'язана насамперед з тим, що в центрах амбулаторної хірургії операції виконуються за новітніми малотравматичними хірургічними методиками, а застосування сучасних препаратів для місцевої анестезії дозволяє уникнути післяопераційних ускладнень, пов'язаних із загальним знеболенням.
</P>
<P>
<div class="title_tab">Показаннями для виконання хірургічних втручань в поліклініці та стаціонарі одного дня є:</div>
<ul>
<li>грижі передньої черевної стінки (вправимі пупкові, пахові, білої лінії живота);</li>
<li>варикозна хвороба нижніх кінцівок;</li>
<li>хронічний геморой II-IV ступеню;</li> 
<li>епітеліальний куприковий хід;</li> 
<li>хронічна анальна тріщина;</li>
<li>фіброзні поліпи анального каналу;</li> 
<li>гострий підшкірний парапроктит;</li>
<li>підшкірно-підслизова нориця прямої кишки;</li> 
<li>доброякісні новоутворення підшкірно-жирової клітковини (ліпоми, атероми, дермоїдні кісти, тріхофолікулярні кісти);</li>
<li>доброякісні новоутворення шкіри (дерматофіброми, невуси, капілярні та кавернозні гемангіоми, ангіофіброми, папіломи, гіпертрофічні та келоїдні рубці, операції при вростаючому нігті);</li>
<li>доброякісні новоутворення молочних залоз;</li> 
<li>варикоцеле;</li> 
<li>фімоз;</li>
<li>водянка яєчка;</li> 
<li>халазіон повік;</li> 
<li>доброякісні пухлини повік і кон'юнктиви (вірусна бородавка, папіломи, атероми, кісти залоз повік, птерігіум);</li> 
<li>операції на сльозовивідних шляхах;</li> 
<li>відкритокутова глаукома;</li>
<li>неускладнені катаракти;</li>
<li>вправлення кісток носу;</li>
<li>пункція гайморових пазух;</li>
</ul>
</P>
