<P>При ЦРП централізовано працює транспортний відділ, що вирішило питання обслуговування викликів, доставки медікаментів, проведення аналізів, ЕКГ вдома, 
перевезення хворих, насамперед, інвалідів, осіб похилого віку, вагітних.<br>
В цілодобовому режимі працює відділення невідкладної медичної допомоги.<br>Набуває розвитку служба амбулаторної хірургії. При ЦРП успішно працює відділення малоінвазивних технологій.<br>
Значна увага приділяється розвитку сімейної медицини.</P></td></tr>
<tr><td><img class="image_left" align="left" src="images/statist.jpg" width="200" height="138" border="0" alt=""></td>
