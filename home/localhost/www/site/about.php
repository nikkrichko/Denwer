<P>Центральну району поліклініку утворено відповідно до рішення сесії Київської міської ради від 15.01.2002 року.
Надання медичної допомоги забезпечують 197  лікарів та 238 середніх медпрацівників. 79 лікарів та 67 осіб середнього медперсоналу працюють в госпрозрахункових умовах.
В первинній ланці охорони здоров'я надають  медичну допомогу 27 дільничних терапевтів, 7 сімейних лікарів, 14 дільничних акушер-гінекологів.
Крім дільничної мережі, в ЦРП проводять прийом лікарі 23 спеціальностей, консультативну 
допомогу надають 26 районних спеціалістів, науковці медичної Академії післядипломної освіти, фахівці міських клінічних лікарень.</P></td></tr>
<tr><td><img class="image_left" align="left"  src="images/rentgen.jpg" width="200" height="138" border="0" alt=""></td>
		<td valign="top" align="left">
<P>Матеріально-технічна база ЦРП практично в повному обсязі забезпечує належний рівень лікувально-діагностичного процесу на догоспітальному етапі.
Рентгенслужбою проводяться всі дослідження, в т.ч. комп'ютерні. 
В клініко-діагностичній лабораторії проводяться загальноклінічні, гематологічні, біохімічні, мікробіологічні, цитологічні, серологічні дослідження.
Діагностичне відділення оснащено апаратурою для функціональної діагностики.
В фізіотерапевтичному відділенні працюють кабінети електролікування, світлолікування, лікувального масажу, ЛФК, оксигенотерапії, лазеротерапії.</P></td></tr>
<tr><td><img class="image_left" align="left" src="images/neotl.jpg" width="200" height="138" border="0" alt=""></td>
