		<div class="title">Пам'ятка з профілактики грипу та інших гострих респіраторних інфекцій (ГРІ)</div>
		<table width="725" class="gen" align="center" border="0" cellpadding="0" cellspacing="0">
        <tr><td><img class="image_left" align="left" src="images/grip1.jpg" width="200" height="138" border="0" alt=""> 
		<P>ГРІ - це велика група гострих вірусних захворювань, які характеризуються повітряно-крапельним шляхом передачі та переважним ураженням верхніх дихальних шляхів. 
		Грип є найбільш поширеним гострим респіраторним вірусним захворюванням, особливість якого у надзвичайно швидкому поширенні і вираженій інтоксикації.&nbsp;
		Механізм передачі вірусу грипу - повітряно-крапельний. Грип може призвести до тяжких ускладнень з боку легеневої, серцево-судинної, нервової, ендокринної систем. <B>
        <br><br><br><br><P>Профілактика захворювань. </B>
        <P>Профілактичні заходи спрямовані насамперед для підвищення резистентності організму до дії збудників грипу, а також інших ГРІ та ГРЗ. 
        <P>Ефективними, особливо у період підвищення захворюваності, фахівці відзначають наступні методи профілактики: <BR>- повноцінне харчування з включенням вітамінів у природному 
		вигляді; <BR>- загартовування та часте провітрювання приміщень; <BR>- вживання загальнозміцнюючих та тонізуючо-імуномодулюючих препаратів та препаратів цілеспрямованої 
		імуностимулюючої дії. 
        <P>Високоефективними є рекомендації загального санітарно-гігієнічного спрямування, зокрема: <BR>- Часто мити руки з милом, особливо після відвідування; 
		<BR>- Прикривати ніс та рот хустинкою (або одноразовими серветками), особливо при кашлі та чиханні; 
		<BR>- Широко застосовувати засоби нетрадиційної (народної) медицини, гомеопатичні препарати, оксолінову мазь. <B>
        <P>На що необхідно звернути увагу:</B> <BR>- Існують деякі особливості клінічного перебігу грипу у різних вікових группах. 
		У дітей молодшого віку передусім можуть з'явитися симптоми нейротоксикозу (блювання, судоми) навіть на тлі субфебрильної температури тіла. 
		У осіб похилого віку на тлі ГРВІ загострюються серцево-судинні захворювання, активуються хронічні вогнища інфекції, що суттєво впливає на клінічні прояви грипу. 
		<BR>- Захворювання здебільшого починається гостро, з проявів загальної інтоксикації (озноб, відчуття жару, сильний головний біль, біль в очах). 
		Хворого турбують загальна слабкість, ломота в попереку, крижах, суглобах, м'язовий біль, порушується сон. 
		Привертає увагу вигляд обличчя хворого: гіперемія, одутлість, блискучі очі — загалом воно нагадує обличчя заплаканої дитини. 
		Інкубаційний період при грипі коливається від декількох годин до 3 діб. <BR>- Здебільшого на другу добу хвороби з'являється сухий кашель, починає турбувати біль у грудях. 
		На 3-5 добу кашель м'якшає, з'являється незначна кількість слизового мокротиння. Носове дихання порушене через набряк слизової оболонки. 
		<BR>- Вже в перші години температура тіла може сягати 39-40°С, утримуватись на такому рівні 2-3 доби. Проте, якщо в подальшому гарячка продовжується або розвивається її 
		друга хвиля (загалом триває довше 5-7 діб), це свідчить про розвиток бактеріальних ускладнень. <BR>- Лікування ГРВІ та грипу є складною проблемою, тому його має визначати лікар.
		Не потрібно займатись самолікуванням, це небезпечно! При своєчасному і правильному лікуванні хвороба закінчується повним одужанням. <B>

